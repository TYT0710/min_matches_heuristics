class Solver_Statistics:

	def __init__(self,elapsed_time,nodes_explored,lower_bound,upper_bound):
		self.elapsed_time = elapsed_time
		self.nodes_explored = nodes_explored
		self.upper_bound = upper_bound
		self.lower_bound = lower_bound
		
	def __repr__(self):
		s=''
		s+='Elapsed time: ' + str(self.elapsed_time) + '\n'
		s+='Nodes explored: ' + str(self.nodes_explored) + '\n'
		s+='Upper bound: ' + str(self.upper_bound) + '\n'
		s+='Lower bound: ' + str(self.lower_bound) + '\n'
		return s
